class BritishCoins
{
	public:
	vector <int> coins(int pence)
	{
		vector<int> c(3, 0);
		c[0] = pence /240;		//pounds, is number of shillings per pound times 
								// number of pennies per shilling
		pence -= c[0] * 240;
		c[1] = pence / 12;		//shillings
		pence -= c[1]*12;		
		c[2] = pence;			// left over pennies
	
		return c;
	}
};




















