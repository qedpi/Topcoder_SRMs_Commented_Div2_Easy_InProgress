class CutoffRounder
{
public:
	int round(string num, string cutoff)
	{
					stringstream s;		//turn strings into doubles
					s << cutoff;
					double limit;
					s >> limit;

					s.str("");
					s.clear();

					s << num;

					double n;
					s >> n;

					if (n - floor(n) >= limit)		// if the decimal portion is >= limit, round upwards
									n = ceil(n);
		return n;
	}
};

















